#Tuplas:
'''
1) É uma lista declarada como uma constante

2) Não é possível adicionar, remover ou alterar seus elementos

3) Toda a tupla será um conjunto de objetos também imutáveis

4) São estruturas de dados heterogêneas
'''
'''
Definição de uma tupla:

Maneira tradicional--> t = tuple(elementos sem nenhum tipo de separação, e entre aspas duplas) 

Maneira --> t = elementos_separados_por_vírgula
'''
#Exemplos:

t = tuple("248468230")
print(type(t))
x = 2,7,8,9,0,4,5,6; print(type(x))
